public class AddTwoIntegers {

        public static void main(String[] args) {

            int first = 10;
            int second = 20;

            int sum = add(first, second);

            System.out.println("The sum is: " + sum);
        }

        public static int add(int int1, int int2) {
            return int1 + int2;
        }
    }
